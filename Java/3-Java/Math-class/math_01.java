
public class math_01 {
	public static void main(String[] args) {
		double number = -5.7;
		
		System.out.println("絶対値："+Math.abs(number));
		System.out.println("平方根："+Math.sqrt(25));
		System.out.println("べき乗："+Math.abs(number));
		System.out.println("絶対値："+Math.pow(2, 3)); //2^3
		System.out.println("最大値："+Math.max(number, 4));
		System.out.println("最小値："+Math.min(number, 4));
	}
}
