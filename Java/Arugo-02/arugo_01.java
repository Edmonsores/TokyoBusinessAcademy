import java.util.Scanner;
public class arugo_01 {
	public static void main (String [] args) {
		Scanner in = new Scanner (System.in);
		System.out.println("変数に値を入力してください。");
		int A=in.nextInt();
		in.close();
		int B;
		if (A < 5) {
			B=A*3;
		}else {
			B=A*2;
		}
		System.out.println("変数は"+B);
	}
}
