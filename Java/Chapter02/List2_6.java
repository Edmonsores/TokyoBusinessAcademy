public class List2_6 {
	public static void main (String [] args) {
		double d; //小数点を含む数値　(Number with decimal point) 64bits
		d = 9.9;
		char c; //一文字　(character) 16bits
		c = 'あ';
		byte b; //８ビット整数符号付　(8-bit integer signed)
		b = 81;
		boolean bool; //真偽値　(true false value)
		bool = true;
		//値＝value
		System.out.println("dの値は"+d);
		System.out.println("cの値は"+c);
		System.out.println("bの値は"+b);
		System.out.println("boolの値は"+bool);
	}
}
