str1=input('整数１を入力してください➞')
str2=input('整数２を入力してください➞')

num1 = int(str1)
num2 = int(str2)

print(str1 + '+' + str2 + '=' +str(num1+num2))
print(str1 + '-' + str2 + '=' +str(num1-num2))
print(str1 + '*' + str2 + '=' +str(num1*num2))
print(str1 + '/' + str2 + '=' +str(num1/num2))
print(str1 + '//' + str2 + '=' +str(num1//num2))
print(str1 + '%' + str2 + '=' +str(num1%num2))
